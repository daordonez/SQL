drop database if exists facturacio;

create database facturacio;

use facturacio;

create table families(
fam varchar(20),
des varchar(50),
primary key(fam)) engine=innodb;

create table magatzems(
mag varchar(20),
des varchar(50),
primary key(mag)) engine=innodb;

create table poblacions(
pob int,
nom varchar(20),
primary key(pob)) engine=innodb;

create table articles(
art varchar(20),
des varchar(50),
preu float(10,2),
fam varchar(20),
primary key(art),
foreign key(fam) references families(fam)) engine=innodb;

create table estocs(
art varchar(20),
mag varchar(20),
estoc int(10),
est_min int(10),
est_max int(10),
primary key(art,mag),
foreign key(art) references articles(art),
foreign key(mag) references magatzems(mag)) engine=innodb;

create table pro(
pro int,
dni varchar(9),
nom varchar(20),
pob int,
primary key(pro),
foreign key(pob) references poblacions(pob)) engine=innodb;

create table preus_pro(
art varchar(20),
pro int,
preu float(10,2),
primary key(art,pro),
foreign key(art) references articles(art),
foreign key(pro) references pro(pro)) engine=innodb;

create table clients(
cli int,
dni varchar(9),
nom varchar(20),
pob int,
dte float(10,2),
primary key(cli),
foreign key(pob) references poblacions(pob)) engine=innodb;

create table capfac(
exer int(10),
num int(10),
cli int,
data date,
cobrat float(10,2),
primary key(exer,num),
foreign key(cli) references clients(cli)) engine=innodb;

create table detfac(
exer int(10),
num int(10),
lin varchar(20),
art varchar(20),
quant int(10),
preu float(10,2),
dte float(10,2),
primary key(exer,num,lin),
foreign key(art) references articles(art),
foreign key(exer,num) references capfac(exer,num)) engine=innodb;


